# Text color codes
RESET = '\033[0m'         # Reset to default color
RED = '\033[31m'          # Red text color
GREEN = '\033[32m'        # Green text color
YELLOW = '\033[33m'       # Yellow text color
BLUE = '\033[34m'         # Blue text color

# Example usage
print(RED + "This text is in red color!" + RESET)
print(GREEN + "This text is in green color!" + RESET)
print(YELLOW + "This text is in yellow color!" + RESET)
print(BLUE + "This text is in blue color!" + RESET)